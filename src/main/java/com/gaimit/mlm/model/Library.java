package com.gaimit.mlm.model;

public class Library extends Pagination {
	private int idLib;
	private String nameLib;
	private String locLib;
	private String stringKeyLib;
	private String expDate;
	private int numOfDigitBarcode;
	private String regDateLib;
	private String purpose;
	private String statementDate;
	private int kioskMode;
	
	
	public int getIdLib() {
		return idLib;
	}
	public void setIdLib(int idLib) {
		this.idLib = idLib;
	}
	public String getNameLib() {
		return nameLib;
	}
	public void setNameLib(String nameLib) {
		this.nameLib = nameLib;
	}
	public String getLocLib() {
		return locLib;
	}
	public void setLocLib(String locLib) {
		this.locLib = locLib;
	}
	public String getStringKeyLib() {
		return stringKeyLib;
	}
	public void setStringKeyLib(String stringKeyLib) {
		this.stringKeyLib = stringKeyLib;
	}
	public String getExpDate() {
		return expDate;
	}
	public void setExpDate(String expDate) {
		this.expDate = expDate;
	}
	public int getNumOfDigitBarcode() {
		return numOfDigitBarcode;
	}
	public void setNumOfDigitBarcode(int numOfDigitBarcode) {
		this.numOfDigitBarcode = numOfDigitBarcode;
	}
	public String getRegDateLib() {
		return regDateLib;
	}
	public void setRegDateLib(String regDateLib) {
		this.regDateLib = regDateLib;
	}
	public String getPurpose() {
		return purpose;
	}
	public void setPurpose(String purpose) {
		this.purpose = purpose;
	}
	public String getStatementDate() {
		return statementDate;
	}
	public void setStatementDate(String statementDate) {
		this.statementDate = statementDate;
	}
	public int getKioskMode() {
		return kioskMode;
	}
	public void setKioskMode(int kioskMode) {
		this.kioskMode = kioskMode;
	}
	
	
}
